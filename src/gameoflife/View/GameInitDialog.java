    private SessionParams sessionParams = null;
    private SessionParams tryCollectSessionParams(){
       boolean isAllInputsCorrect = true;
       SessionParams sp = new SessionParams();
       
       Players.PlayerParameters firstPlayer = new Players.PlayerParameters();
       firstPlayer.setName( firstPlayerName.getText());
       firstPlayer.setColor( ChoseColorFirstPlayerButton.getBackground());
       Players.PlayerParameters secondPlayer = new Players.PlayerParameters();
       secondPlayer.setName( secondPlayerName.getText());
       secondPlayer.setColor(ChoseColorSecondPlayerButton.getBackground());
       
       sp.addPlayer(firstPlayer);
       sp.addPlayer(secondPlayer);
       
       int epochCount = (int) epochCountSpinner.getModel().getValue();
       int firstMoveActions = (int) firstMoveActionsCountSpinner.getModel().getValue();
       int moveActions = (int) actionsCountSpinner.getModel().getValue();
       
       sp.setEpochCount(epochCount);
       sp.setFirstMoveActionsCount(firstMoveActions);
       sp.setMoveActionsCount(moveActions);
       
       sp.setFieldShapeParams((SessionParams.FieldShapeParams) fieldShapeList.getModel().getSelectedItem());
       
       return sp;
    } 
    
    public SessionParams getSessionParams(){
        return sessionParams;
    }
    
    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        boolean isAllInputsCorrect = true;
        if ( isAllInputsCorrect){
            SessionParams sp = tryCollectSessionParams();
            if ( sp != null){
                this.sessionParams = sp;
                setVisible(false); 
                dispose();
            }
        }
    }//GEN-LAST:event_startButtonActionPerformed

