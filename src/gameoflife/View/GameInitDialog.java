    private void initModels(){
        fieldShapeList.setModel(new FieldShapesModel());
        epochCountSpinner.setModel( new PositiveIntSM());
        firstMoveActionsCountSpinner.setModel( new PositiveIntSM());
        actionsCountSpinner.setModel(new PositiveIntSM());
        
    }
    
    private class PositiveIntSM extends SpinnerNumberModel{

        @Override
        public Object getPreviousValue() {
            Integer curVal = 0; 
            curVal = Integer.valueOf(getValue().toString());
            return  curVal > 1 ? curVal - 1 : 1;
            
        }
    }

    private class FieldShapesModel extends AbstractListModel implements ComboBoxModel {

        ArrayList<SessionParams.FieldShapeParams> shapes = SessionParams.getFieldShapeNames();
        SessionParams.FieldShapeParams selectedItem = shapes.get(0);
        
        @Override
        public int getSize() {
            return shapes.size();
        }

        @Override
        public Object getElementAt(int i) {
            return shapes.get(i);
        }

        @Override
        public void setSelectedItem(Object o) {
            selectedItem = (SessionParams.FieldShapeParams) o;
        }

        @Override
        public Object getSelectedItem() {
            return selectedItem;
        }
    }
    
